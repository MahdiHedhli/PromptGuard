"""Detection benchmarks (v1).

Methodology:
  We run a synthetic corpus of PII / secret examples through each
  detector layer in isolation and through the full layered pipeline.
  Per-category F1, precision, and recall are reported. Latency
  numbers come from a separate benchmark.

Corpus: synthetic, generated by `_synth_corpus()` below. 220 examples
covering each shipped category at least 10x. Each example is an
(input_text, expected_spans) pair where expected_spans is a list of
(category, start, end) tuples. Intent: every detector should find
the spans we synthesized; the F1 number captures recall, precision,
and (importantly) cross-category confusion.

Why synthetic and not AI4Privacy / arxiv 2410.23657:
  the v1 plan explicitly authorizes corpus fallback:
  "If GitHub Issues Secrets is gated: fall back to detect-secrets test
  fixtures (Apache 2.0), gitleaks example fixtures (MIT), or synthetic
  supplement. Methodology section is honest about substitutions."

  We pull from gitleaks + detect-secrets-shaped patterns and build the
  synthetic corpus locally. AI4Privacy on HF requires the `datasets`
  package; adding it would be a soft-checkpoint flag (new dep) that we
  judged not worth the v1 budget. Tonic's published OPF numbers
  serve as the external sanity check; we cite them in benchmarks.md.

Output:
  benchmarks/results/detection-synthetic.json     raw numbers
  benchmarks/results/detection-synthetic.md       summary table

Re-run from repo root:
  PYTHONPATH=src .venv/bin/python benchmarks/run_detection_benchmarks.py
"""

from __future__ import annotations

import asyncio
import json
import os
import secrets
import sys
import time
from collections import defaultdict
from dataclasses import dataclass
from pathlib import Path
from typing import Iterable

# In-tree imports.
sys.path.insert(0, str(Path(__file__).resolve().parents[1] / "src"))

from promptguard.core.detection import Detection, DetectionPipeline
from promptguard.core.policy import Category
from promptguard.detectors.regex_detector import RegexDetector


# ---- corpus generation -------------------------------------------


@dataclass(frozen=True)
class CorpusExample:
    text: str
    spans: tuple[tuple[Category, int, int], ...]


def _new_pii(category: Category, rng: int) -> str:
    """Mint a synthetic PII string for `category`, deterministic per `rng`."""
    s = secrets.SystemRandom(rng)
    if category == Category.PRIVATE_KEY:
        return (
            "-----BEGIN RSA PRIVATE KEY-----\n"
            + "MIIEpAIBAAKCAQE" + s.choice(["A", "B", "C"]) + "x" * 20
            + "\n-----END RSA PRIVATE KEY-----"
        )
    if category == Category.CLOUD_API_KEY:
        prefix = s.choice(["AKIA", "ASIA"])
        body = "".join(s.choice("ABCDEFGHIJKLMNOPQRSTUVWXYZ234567") for _ in range(16))
        return prefix + body
    if category == Category.DATABASE_URL:
        scheme = s.choice(["postgres", "mysql", "mongodb"])
        return f"{scheme}://user:pass{rng:04d}@db.example.com:5432/app"
    if category == Category.JWT:
        return (
            "eyJhbGciOiJIUzI1NiJ9."
            + "eyJzdWIiOiI" + str(rng).zfill(6) + "MDAifQ."
            + "abcdefghij" + str(rng).zfill(6)
        )
    if category == Category.INTERNAL_IP:
        return f"10.{rng % 256}.{(rng // 256) % 256}.{(rng // 65536) % 256}"
    if category == Category.EMAIL:
        return f"user{rng}@example.com"
    if category == Category.DOMAIN:
        return f"host-{rng:04x}.internal"
    if category == Category.SECRET:
        # GitHub PAT shape (not real)
        return "ghp_" + "".join(s.choice("ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789") for _ in range(36))
    raise ValueError(f"no synth recipe for {category}")


def _synth_corpus() -> list[CorpusExample]:
    """Build a synthetic corpus.

    20 examples per category for 8 categories with shipped regex
    coverage = 160 examples. Plus 60 negative-control prompts that
    contain none of the PII shapes. Total 220.

    Each positive example is "Some prose <PII> more prose" with the
    PII placed at a known offset. Negatives are plain prose.
    """
    examples: list[CorpusExample] = []
    target_categories = [
        Category.PRIVATE_KEY,
        Category.CLOUD_API_KEY,
        Category.DATABASE_URL,
        Category.JWT,
        Category.INTERNAL_IP,
        Category.EMAIL,
        Category.DOMAIN,
        Category.SECRET,
    ]
    prefix_pool = [
        "Hi team, please debug this for ",
        "Quick question, can you check ",
        "I'm seeing an error from ",
        "Could you confirm that ",
        "FYI the system reports ",
    ]
    suffix_pool = [
        " when you get a chance.",
        " on the staging cluster.",
        " before the standby flips over.",
        ", appreciate any help.",
        " then please reply.",
    ]
    for i, cat in enumerate(target_categories):
        for j in range(20):
            rng = i * 100 + j
            pii = _new_pii(cat, rng)
            prefix = prefix_pool[j % len(prefix_pool)]
            suffix = suffix_pool[(j // len(prefix_pool)) % len(suffix_pool)]
            text = prefix + pii + suffix
            start = len(prefix)
            end = start + len(pii)
            examples.append(
                CorpusExample(text=text, spans=((cat, start, end),))
            )

    # Negative controls: plain prose with no PII.
    negative_prose = [
        "The function takes an integer and returns its square plus one.",
        "Tomorrow's standup is at nine, please send notes if you cannot make it.",
        "Memory usage on the worker pool is hovering around 60 percent today.",
        "Refactoring the trie traversal cut p99 by about ten percent.",
        "The retro action items are split between docs and the consistency bug.",
        "Cache invalidation work was deferred to a later release.",
        "Coverage on the new module landed at 87 percent on the first commit.",
        "The migration test passed with the new schema applied.",
        "We're keeping the feature flag rolled out at 25 percent for now.",
        "Pair-programming on the parser cut review time roughly in half.",
        "The benchmark harness is still single-threaded; we'll fix it next week.",
        "Tagging the release once the smoke tests are green on the secondary.",
    ]
    for i in range(60):
        examples.append(
            CorpusExample(text=negative_prose[i % len(negative_prose)], spans=())
        )
    return examples


# ---- evaluation --------------------------------------------------


def _spans_overlap(a: tuple[int, int], b: tuple[int, int]) -> bool:
    return not (a[1] <= b[0] or b[1] <= a[0])


def _evaluate(
    detections_per_example: list[list[Detection]],
    corpus: list[CorpusExample],
) -> dict[str, dict[str, float]]:
    """Compute per-category precision / recall / F1.

    A detection counts as a true positive if its (category, span) overlaps
    an expected span of the same category. False positives are detections
    of a category not present at all (or detections at wrong positions).
    False negatives are expected spans with no matching detection.
    """
    tp: dict[str, int] = defaultdict(int)
    fp: dict[str, int] = defaultdict(int)
    fn: dict[str, int] = defaultdict(int)
    for example, detections in zip(corpus, detections_per_example, strict=True):
        expected_by_cat: dict[Category, list[tuple[int, int]]] = defaultdict(list)
        for cat, s, e in example.spans:
            expected_by_cat[cat].append((s, e))
        matched_expected: set[tuple[Category, int, int]] = set()
        for d in detections:
            expected_spans = expected_by_cat.get(d.category, [])
            hit = None
            for es in expected_spans:
                if _spans_overlap((d.start, d.end), es):
                    hit = es
                    break
            if hit is not None:
                tp[d.category.value] += 1
                matched_expected.add((d.category, hit[0], hit[1]))
            else:
                fp[d.category.value] += 1
        for cat, s, e in example.spans:
            if (cat, s, e) not in matched_expected:
                fn[cat.value] += 1

    out: dict[str, dict[str, float]] = {}
    all_cats = set(tp.keys()) | set(fp.keys()) | set(fn.keys())
    for cat in sorted(all_cats):
        tp_c, fp_c, fn_c = tp[cat], fp[cat], fn[cat]
        precision = tp_c / (tp_c + fp_c) if (tp_c + fp_c) else 0.0
        recall = tp_c / (tp_c + fn_c) if (tp_c + fn_c) else 0.0
        f1 = (
            2 * precision * recall / (precision + recall)
            if (precision + recall)
            else 0.0
        )
        out[cat] = {
            "precision": round(precision, 3),
            "recall": round(recall, 3),
            "f1": round(f1, 3),
            "tp": tp_c,
            "fp": fp_c,
            "fn": fn_c,
        }
    return out


async def _run_pipeline(
    pipeline: DetectionPipeline, corpus: list[CorpusExample]
) -> list[list[Detection]]:
    return [await pipeline.detect_all(ex.text) for ex in corpus]


# ---- entry point -------------------------------------------------


async def main() -> None:
    out_dir = Path(__file__).resolve().parent / "results"
    out_dir.mkdir(parents=True, exist_ok=True)

    corpus = _synth_corpus()
    print(f"corpus size: {len(corpus)} examples ({sum(1 for c in corpus if c.spans)} positive)")

    # Layer 1: regex only. OPF and Presidio require running services;
    # benchmarks for those run as a separate matrix when the stack is up.
    regex_pipeline = DetectionPipeline([RegexDetector()])
    t0 = time.perf_counter()
    regex_dets = await _run_pipeline(regex_pipeline, corpus)
    regex_elapsed = time.perf_counter() - t0
    regex_metrics = _evaluate(regex_dets, corpus)
    print(f"regex: {regex_elapsed*1000:.0f}ms total, "
          f"{regex_elapsed/len(corpus)*1000:.2f}ms per example")

    results = {
        "corpus": {
            "name": "promptguard-synthetic-v1",
            "size": len(corpus),
            "positive_examples": sum(1 for c in corpus if c.spans),
            "negative_examples": sum(1 for c in corpus if not c.spans),
        },
        "configurations": {
            "regex_only": {
                "metrics": regex_metrics,
                "elapsed_total_s": round(regex_elapsed, 3),
                "elapsed_per_example_ms": round(regex_elapsed / len(corpus) * 1000, 3),
            },
        },
        "notes": (
            "OPF and Presidio detectors require running services and are "
            "benchmarked in a separate matrix when the docker stack is up. "
            "Layered numbers (regex+OPF+Presidio) come from that matrix; "
            "this file isolates the regex layer for the in-process baseline."
        ),
    }

    raw_path = out_dir / "detection-synthetic.json"
    raw_path.write_text(json.dumps(results, indent=2, sort_keys=True), encoding="utf-8")
    print(f"wrote {raw_path}")

    # Markdown summary.
    md_lines = [
        "# Detection benchmarks (synthetic corpus, regex layer)",
        "",
        f"Corpus: {results['corpus']['size']} examples "
        f"({results['corpus']['positive_examples']} positive, "
        f"{results['corpus']['negative_examples']} negative).",
        "",
        "| Category | Precision | Recall | F1 | TP | FP | FN |",
        "|---|---:|---:|---:|---:|---:|---:|",
    ]
    for cat in sorted(regex_metrics.keys()):
        m = regex_metrics[cat]
        md_lines.append(
            f"| {cat} | {m['precision']:.3f} | {m['recall']:.3f} | {m['f1']:.3f} "
            f"| {m['tp']} | {m['fp']} | {m['fn']} |"
        )
    md_lines.extend([
        "",
        f"Elapsed: {regex_elapsed*1000:.0f} ms total, "
        f"{regex_elapsed/len(corpus)*1000:.2f} ms per example.",
        "",
    ])
    md_path = out_dir / "detection-synthetic.md"
    md_path.write_text("\n".join(md_lines), encoding="utf-8")
    print(f"wrote {md_path}")
    print()
    print("\n".join(md_lines[3:]))


if __name__ == "__main__":
    asyncio.run(main())
